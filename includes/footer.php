<script>
    // Initialize Swiper
    const swiper = new Swiper('.heroSwiper', {
        loop: true,
        effect: 'fade',
        fadeEffect: {
            crossFade: true
        },
        speed: 1000,
        autoplay: {
            delay: 5000,
        },
        pagination: {
            el: '.swiper-pagination',
            clickable: true,
        },
        navigation: {
            nextEl: '.swiper-button-next',
            prevEl: '.swiper-button-prev',
        },
    });

    // Initialize AOS
    AOS.init({
        duration: 1000,
        once: true,
        offset: 100
    });

    // Scroll Effects (Navbar & Back to Top)
    const mainNav = document.getElementById('mainNav');
    const backToTop = document.getElementById('backToTop');

    window.addEventListener('scroll', function () {
        // Navbar scroll effect
        if (window.scrollY > 50) {
            mainNav.classList.add('navbar-scrolled');
        } else {
            mainNav.classList.remove('navbar-scrolled');
        }

        // Back to top visibility
        if (window.scrollY > 400) {
            backToTop.classList.add('show');
        } else {
            backToTop.classList.remove('show');
        }
    });

    // Cookie Consent Logic
    document.addEventListener('DOMContentLoaded', function () {
        // 1. Clean URL extension
        if (window.location.pathname.endsWith(".php")) {
            const cleanUrl = window.location.pathname.replace(".php", "");
            window.history.replaceState({}, "", cleanUrl + window.location.search);
        }

        const cookieConsent = document.getElementById('cookieConsent');
        const acceptCookies = document.getElementById('acceptCookies');

        if (!localStorage.getItem('elite_cookies_accepted')) {
            if (cookieConsent) {
                cookieConsent.classList.add('show');
            }
        }

        if (acceptCookies) {
            acceptCookies.addEventListener('click', function () {
                localStorage.setItem('elite_cookies_accepted', 'true');
                if (cookieConsent) {
                    cookieConsent.classList.remove('show');
                }
            });
        }
    });
</script>

